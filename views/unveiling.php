            <h2>1. Introduction</h2>

            <p>Il y a 150 ans, le 15 avril 1874, une exposition à Paris réunissait des artistes, notamment des peintres,
                des sculpteurs, des graveurs, tous exclus parce que leur style était considéré comme incomplet. Ce
                collectif est devenu connu sous le nom d'Impressionnistes, un terme tiré du célèbre tableau de Claude
                Monet “Impression, soleil levant”. Leur travail a été critiqué à l'époque pour son imprécision. Des
                figures éminentes telles que Renoir, Degas, Monet et Sisley ont participé à cette exposition, aux côtés
                de la seule femme du groupe, Berthe Morisot.</p><br>

            <p>Avant de plonger dans les détails, passons le relais à notre experte résidente en histoire de l'art,
                notre guide à travers les corridors du temps. Je vous prie de vous joindre à moi pour accueillir le Dr
                Emily Richard, titulaire d'un doctorat en histoire de l'art de la prestigieuse École du Louvre à Paris,
                France. Dr Richard, la scène est à vous.</p><br>

